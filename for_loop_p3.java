package com.company;
// Write a java to find the sum of first 10 natural numbers.
public class for_loop_p3 {
    public static void main(String[] args) {
        int sum = 0;
        for(int i =1;i<=10; i++){
            sum += i;
        }
        System.out.println("The value of N natural number is:"+ sum);
    }
}
